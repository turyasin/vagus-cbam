export function searchCbamCodes(query: string): CbamCode[] {
  if (!query || query.trim().length < 2) return [];

  const normalized = query.toLowerCase().trim();

  return cbamCodes.filter(
    (code) =>
      code.cn.replace(/\s/g, "").includes(normalized.replace(/\s/g, "")) ||
      code.turkishDescription.toLowerCase().includes(normalized) ||
      code.description.toLowerCase().includes(normalized) ||
      (code.notes && code.notes.toLowerCase().includes(normalized))
  );
}

export function filterBySector(sector: string): CbamCode[] {
  if (!sector) return cbamCodes;
  return cbamCodes.filter((c) => c.sector === sector);
}
